package com.socialflow.common.enums;

/**
 * 多Agent协作中的角色枚举
 *
 * 【作用】在复杂的文案生成任务中，系统采用"多Agent协作"模式：
 *   多个具有不同职责的AI角色（Agent）按顺序协作，共同完成一篇高质量文案的生成。
 *
 * 【协作流程】
 *   PLANNER（规划者）→ WRITER（写作者）→ REVIEWER（审核者）→ OPTIMIZER（优化者）
 *
 *   1. PLANNER 分析用户需求，制定文案大纲和写作策略
 *   2. WRITER 根据大纲撰写文案初稿
 *   3. REVIEWER 审核初稿，检查质量和合规性，提出修改建议
 *   4. OPTIMIZER 根据审核意见进行最终优化和润色
 *
 * 【为什么需要多Agent】
 *   单个AI一次生成的文案质量有限，通过多个角色"分工合作"，
 *   模拟人类团队的创作流程（策划→写手→编辑→校对），
 *   可以显著提升文案质量。
 */
public enum AgentRole {

    /** 规划者——分析用户需求，制定文案大纲、确定写作风格和关键要点 */
    PLANNER,

    /** 写作者——根据规划者制定的大纲，撰写文案初稿 */
    WRITER,

    /** 审核者——对初稿进行质量审核，检查内容准确性、语法、合规性等 */
    REVIEWER,

    /** 优化者——根据审核意见对文案进行最终优化、润色和排版 */
    OPTIMIZER
}
